/**
 * ПРОЕКТЫ НЕДЕЛИ — правила редактирования (для владельца):
 *
 * Как это работает:
 * - каждую неделю приложение берёт один проект из списка (по кругу, у всех
 *   учеников в одну неделю — один и тот же);
 * - понедельник–пятница ученик получает ОДИН шаг этого проекта как задание дня,
 *   в субботу и воскресенье — свободные задания из общего списка
 *   (quests-config.ts), чтобы серия дней не сгорала на выходных;
 * - сам проект висит во вкладке «Неделя» и закрывается в конце.
 *
 * Что можно менять свободно: title, why, описания шагов, result, награды.
 * Что менять НЕЛЬЗЯ: id проектов и шагов — по ним считается, что уже сдано.
 *
 * Правила для шагов:
 * - название до 40 символов;
 * - описание 1–2 предложения: что именно сделать;
 * - result — что должно быть на скриншоте, это видит куратор;
 * - шаг рассчитан на 20–40 минут и всегда заканчивается видимой картинкой.
 */

export type ProjectStep = {
  id: string;
  title: string;
  description: string;
  // Что ученик показывает куратору — попадает в задание отдельной строкой.
  result: string;
  xpReward: number;
  goldReward: number;
};

export type WeeklyProject = {
  id: string;
  title: string;
  why: string;
  xpReward: number;
  goldReward: number;
  steps: ProjectStep[];
};

