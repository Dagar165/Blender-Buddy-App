export const BUBBLE_EVERY = 18;

const isAllowed = (line: CommunityLine) => MARATHON_OPEN || !line.marathon;

/**
 * Фраза из списка. seed — любое растущее число (номер захода, id медали):
 * так фразы идут по кругу, а не выпадают одинаковыми подряд.
 */
export function pickCommunityLine(
  lines: CommunityLine[],
  seed: number
): string | null {
  const allowed = lines.filter(isAllowed);
  if (allowed.length === 0) return null;

  return allowed[Math.abs(Math.floor(seed)) % allowed.length].text;
}
