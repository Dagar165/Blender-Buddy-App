export type QuestTab = "daily" | "weekly";

export type QuestDefinition = {
  id: string;
  title: string;
  description: string;
  xpReward: number;
  goldReward: number;
  // Что показать куратору на скриншоте (у шагов проекта недели).
  result?: string;
  // «Шаг 2 из 5 — Меч героя»: связывает день с проектом недели.
  stepLabel?: string;
  // Чем задание является на экране: шаг проекта — главное дело дня,
  // разминка — короткая и второстепенная. Влияет только на оформление.
  kind?: "step" | "warmup";
};

type QuestTabContent = {
  tabLabel: string;
};

export type QuestsConfig = {
  tabs: Record<QuestTab, QuestTabContent>;
};

/**
 * РАЗМИНКИ — второе задание буднего дня.
 *
 * Шаг проекта недели — главное дело дня, но он один и требует настроя.
 * Разминка стоит рядом: пять-десять минут, один приём, всегда получается.
 * Смысл в том, чтобы день никогда не выглядел неподъёмным — даже уставший
 * ребёнок откроет Blender и что-то сделает, а серия дней не оборвётся.
 *
 * Поэтому правила жёстче, чем у шагов проекта:
 * - делается за 5–10 минут и не требует доделывать вчерашнее;
 * - один приём за раз, без «а ещё попробуй»;
 * - результат виден сразу и снимается одним скриншотом;
 * - награда заметно меньше шага — главное дело дня всё-таки проект.
 *
 * Каждый будний день выдаётся одна разминка; за неделю они не повторяются.
 */
export const WARMUP_XP = 40;
export const WARMUP_GOLD = 25;

