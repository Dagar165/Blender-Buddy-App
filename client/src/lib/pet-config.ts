export const getPetStage = (level: number): PetStage => {
  let stage = PET_STAGES[0];

  for (const candidate of PET_STAGES) {
    if (level >= candidate.fromLevel) stage = candidate;
  }

  return stage;
};

export const getNextPetStage = (level: number): PetStage | null => {
  return PET_STAGES.find((stage) => stage.fromLevel > level) ?? null;
};

// Предыдущая стадия — та, из которой призрак только что вырос.
// У самой первой её нет, и это правильно: рождение не эволюция.
export const getPreviousPetStage = (stage: PetStage): PetStage | null => {
  const index = PET_STAGES.indexOf(stage);

  return index > 0 ? PET_STAGES[index - 1] : null;
};

